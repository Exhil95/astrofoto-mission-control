from __future__ import annotations

import argparse
import json
import sqlite3
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import psycopg
from psycopg import Connection
from psycopg.types.json import Jsonb

from .profiles import PROFILE_COLUMNS, PROFILE_OWNER_COLUMN
from .session_archive import ARCHIVE_COLUMNS, ARCHIVE_OWNER_COLUMN
from .settings import get_settings


@dataclass(frozen=True)
class MigrationSnapshot:
    profiles: list[dict[str, Any]]
    session_archives: list[dict[str, Any]]

    @property
    def total_rows(self) -> int:
        return len(self.profiles) + len(self.session_archives)


